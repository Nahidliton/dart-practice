void main(){
    String Grade="A";
    switch(Grade){
      case "A":
        print("Excellent");
        break;
      case "B":
        print("Very Good");
        break;
      case "C":
        print("Good");
        break;
      case "D":
        print("Pass");
        break;
      case "F":
        print("Fail");
        break;
      default:
        print("Invalid Grade");
    }
}